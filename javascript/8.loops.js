// Iterate over numbers from 1 to 15
for (let i = 1; i <= 15; i++) {
    // Check if the number is odd by checking if the remainder of division by 2 is not equal to 0
    if (i % 2 != 0) {
        // If the number is odd, log it to the console
        console.log(i);
    }
}
