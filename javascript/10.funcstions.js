// Define a function 'malti' that takes a parameter 'a'
function malti(a) {
    // Iterate from 1 to 10
    for (let i = 1; i <= 10; i++) {
        // Multiply 'a' by the current iteration value 'i' and print the result
        console.log(`${a} * ${i} = ${a * i}`);
    }
}

// Call the 'malti' function with an argument of 5
malti(5);

